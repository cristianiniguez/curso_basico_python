# Operadores lógicos

es_estudiante = True
trabaja = False

es_estudiante and trabaja # False
es_estudiante or trabaja # True
not es_estudiante # False
not trabaja # True

# Operadores de comparación

numero1 = 5
numero2 = 5
numero3 = 7

numero1 == numero2 # True
numero1 == numero3 # False
numero1 != numero3 # True
numero1 > numero3 # False
numero1 < numero3 # True
numero1 >= numero3 # False
numero1 >= numero2 # True
numero1 <= numero2 # True
numero1 <= numero3 # True
