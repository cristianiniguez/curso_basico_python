# Operadores Aritméticos en Python

5 + 5 # 10 (Suma)
5 - 4 # 1 (Resta)
5 * 2 # 10 (Multiplicación)
21 / 5 # 4.2 (División exacta)
21 // 5 # 4 (División entera)
21 % 5 # 1 (Resto)
2 ** 2 # 4 (Potencia)

